class Kullanici {
    String ad, soyad;
    int dogumYili;

    public Kullanici(String ad, String soyad) throws Exception {
        if (ad.equals("") || soyad.equals("")) {
            throw new Exception("Ad veya soyad bos olamaz");
        }
        this.ad = ad;
        this.soyad = soyad;
    }

    public String getAd() {
        return ad;
    }

    public void setAd(String ad) {
        this.ad = ad;
    }

    public String getSoyad() {
        return soyad;
    }

    public void setSoyad(String soyad) {
        this.soyad = soyad;
    }

    public int getDogumYili() {
        return dogumYili;
    }

    public void setDogumYili(String dogumYili) throws NumberFormatException, Exception {
        try {
            this.dogumYili = Integer.parseInt(dogumYili);
            if (2024 - this.dogumYili > 130) {
                throw new Exception("Dogum yili 130 yildan once olamaz");
            }
        } catch (NumberFormatException e) {
            throw e;
        } catch (Exception e) {
            throw e;
        }
    }
    
}

